var logic = {
    add: function (arg1, arg2) {
        return arg1 + arg2;
    },

    subtract: function subtract(arg1, arg2) {
        return arg1 - arg2;
    },

    multiply: function (arg1, arg2) {
        return arg1 * arg2;
    },

    divide: function (arg1, arg2) {
        return arg1 / arg2;
    }
};